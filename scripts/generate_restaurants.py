def slugify(name: str, idx: int) -> str:
    """Латинський slug за можливості; інакше стабільний id за індексом."""
    s = unicodedata.normalize("NFKD", name)
    s = s.encode("ascii", "ignore").decode("ascii")
    s = s.lower()
    s = re.sub(r"[^a-z0-9]+", "-", s).strip("-")
    if len(s) < 2:
        return f"kyiv-{idx}"
    return f"{s[:48].strip('-')}-{idx}"


def main() -> None:
    seen: set[str] = set()
    out: list[dict] = []
    max_n = 201
    for i, (name, cuisine, addr) in enumerate(RAW[:max_n]):
        base = slugify(name, i)
        sid = base
        j = 0
        while sid in seen:
            j += 1
            sid = f"{base}-{j}"
        seen.add(sid)
        out.append(
            {
                "id": sid,
                "name": name,
                "cuisine": cuisine,
                "address": addr,
                "photo": PHOTOS[i % len(PHOTOS)],
            }
        )

    root = Path(__file__).resolve().parents[1]
    target = root / "js" / "data" / "restaurants.js"
    lines = [
        "/** Реальні та відомі київські заклади (адреси в межах Києва). */",
        "window.KYIV_RESTAURANTS = [",
    ]
    for o in out:
        lines.append("  {")
        lines.append(f"    id: {json.dumps(o['id'], ensure_ascii=False)},")
        lines.append(f"    name: {json.dumps(o['name'], ensure_ascii=False)},")
        lines.append(f"    cuisine: {json.dumps(o['cuisine'], ensure_ascii=False)},")
        lines.append(f"    address: {json.dumps(o['address'], ensure_ascii=False)},")
        lines.append(f"    photo: {json.dumps(o['photo'])},")
        lines.append("  },")
    lines.append("];")

    target.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print("Wrote", target, "count=", len(out))


if __name__ == "__main__":
    main()
